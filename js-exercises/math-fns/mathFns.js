// Don't change the export values.
// You can use the function in the Math module

function sqrt(SquareRoot) {
  return Math.sqrt(SquareRoot);
}
function power(number, powerOfNumber) {
  return Math.pow(number, powerOfNumber);
}
function round(decimalNumber) {
  return Math.round(decimalNumber);
}

export { sqrt, power, round };
